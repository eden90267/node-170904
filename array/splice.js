// 在任何位置加入或移除元素

// splice(開始修改的地方, 移除的元素數量(不想移除就使用0), [...要加入的元素])

// * 就地修改

const arr = [1, 5, 7];
arr.splice(1, 0, 2, 3, 4); // [1, 2, 3, 4, 5, 7]
arr.splice(5, 0, 6);       // [1, 2, 3, 4, 5, 6, 7]
arr.splice(1, 2);          // [1, 4, 5, 6, 7]
arr.splice(2, 1, 'a', 'b');// [1, 4, 'a', 'b', 6, 7]
console.log(arr);